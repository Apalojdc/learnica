    <h1>Démarrer en Mode Sans Échec sur Windows 10 et 11</h1>
    <ol>
        <li data-number="1️⃣">
            <h2>Redémarrer le PC</h2>
            <p>cliquez sur le bouton <a href="#">« Démarrer »</a> en bas à gauche de l’écran. Ensuite, cliquez sur l’icône <a href="#">« Alimentation »</a>.</p>
        </li>
        <li data-number="2️⃣">
            <h2>Maintenez la touche « Maj » (ou « Shift ») enfoncée et sélectionnez « Redémarrer »</h2>
            <p></p>
        </li>
        <li data-number="3️⃣">
            <h2>Accéder aux options de démarrage</h2>
            <p>le PC va redémarrer et afficher un écran bleu avec plusieurs options. Sélectionnez <a href="#">« Dépannage »</a>, puis <a href="#">« Options avancées »</a>. Cliquez ensuite sur <a href="#">« Paramètres de démarrage »</a> et enfin sur <a href="#">« Redémarrer »</a>.</p>
        </li>
        <li data-number="4️⃣">
            <h2>Choisir le mode sans échec</h2>
            <p>après le redémarrage, une liste d’options de démarrage apparaît. Utilisez les touches de fonction (F1, F2, etc.) pour sélectionner l’option 4 ou 5 pour démarrer en mode sans échec. L’option 4 est le mode sans échec standard. L’option 5 inclut la connexion réseau.</p>
        </li>
    </ol>
